Este grupo de amigos tem como propósito organizar viagens, trilhas, passeios, travessias, caminhadas, escaladas, rapel e não possuímos fins lucrativos.Buscamos também fazer amizades e muito contato com a natureza! Aventura no geral será bem vinda!

O que é ser Caveira?
É uma filosofia de vida onde a cada dia você supera os seus limites físicos e psicológicos através da prática de esportes e contato direto com a natureza.

O nosso grupo de amigos é pautado no respeito mútuo, na amizade, na solidariedade e na prática de fazer o bem ao próximo.Não fazemos distinção de sexo, orientação sexual, raça, cor, opinião política, religião e outros.

Não compactuamos com intolerâncias, atos discriminatórios ou quaisquer formas exclusão.Que pensemos nisso antes de agir e falar.

